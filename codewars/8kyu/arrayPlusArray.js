function arrayPlusArray(arr1, arr2) {
    let i = 0;
    while (i < arr1.length) {
        arr2.push(arr1[i]);
        i++;
    }

    let sum = 0;

    for (let i = 0; i < arr2.length; i++) {
        sum += arr2[i];
    }
    return sum;
}

